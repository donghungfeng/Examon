function myFunction1(){
    var x = document.querySelectorAll('div.box');
    var tagInput = document.getElementsByTagName('input');
    for(var i=0;i<3;i++){
      if (tagInput[i].checked == true){
        x[0].style.background = '#d9edfe';
      }
    }
 }
